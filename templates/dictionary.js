    function formatDefinition(definitionText) {
        const parts = definitionText.split(' - ');
        if (parts.length === 2) {
            return `<strong>${parts[0].trim()}</strong> - <em>${parts[1].trim()}</em>`;
        }
        return definitionText;
    }

    function displayWordsByLetter(letter, container) {
        const lang = container.dataset.lang;
        const wordListUl = container.querySelector('#wordList');
        wordListUl.innerHTML = '';

        const currentDictionary = dictionaries[lang];
        const sortedWords = Object.keys(currentDictionary).sort();
        const filteredWords = sortedWords.filter(word => word.toLowerCase().startsWith(letter.toLowerCase()));

        if (filteredWords.length > 0) {
            filteredWords.forEach(key => {
                const li = document.createElement('li');
                li.innerHTML = formatDefinition(currentDictionary[key]);
                wordListUl.appendChild(li);
            });
        } else {
            const li = document.createElement('li');
            li.innerHTML = lang === 'pol' ? `<em>Brak słów na literę "${letter}" w słowniku.</em>` : `<em>There aren't words starting with "${letter}" in the dictionary.</em>`;
            wordListUl.appendChild(li);
        }

        container.querySelector('#allWordsList').style.display = 'block';
        container.querySelector('#singleResult').style.display = 'none';

        container.querySelectorAll('.alphabet-filter span').forEach(span => {
            span.classList.remove('active');
        });
        container.querySelector(`.alphabet-filter span[data-letter="${letter}"]`).classList.add('active');
    }

    function searchWord(container) {
        const lang = container.dataset.lang;
        const searchInput = container.querySelector('#searchInput');
        const singleResultDiv = container.querySelector('#singleResult');
        const allWordsListDiv = container.querySelector('#allWordsList');
        const currentDictionary = dictionaries[lang];

        const searchTerm = searchInput.value.toLowerCase().trim();

        singleResultDiv.classList.remove('error');
        singleResultDiv.innerHTML = "";

        if (searchTerm === "") {
            displayWordsByLetter('A', container);
            return;
        }

        allWordsListDiv.style.display = 'none';
        singleResultDiv.style.display = 'block';

        if (currentDictionary[searchTerm]) {
            singleResultDiv.innerHTML = formatDefinition(currentDictionary[searchTerm]);
        } else {
            singleResultDiv.innerHTML = lang === 'pol' ? `<em>Brak słowa "${searchTerm}" w słowniku.</em>` : `<em>There is no word "${searchTerm}" in the dictionary.</em>`;
            singleResultDiv.classList.add('error');
        }
    }

    function resetView(container) {
        const searchInput = container.querySelector('#searchInput');
        if (searchInput.value.trim() === "") {
            const activeLetterSpan = container.querySelector('.alphabet-filter span.active');
            const activeLetter = activeLetterSpan ? activeLetterSpan.dataset.letter : 'A';
            displayWordsByLetter(activeLetter, container);
            container.querySelector('#singleResult').innerHTML = "";
        }
    }

    function initializeAlphabetFilter(container) {
        const lang = container.dataset.lang;
        const alphabetFilterDiv = container.querySelector('#alphabetFilter');
        const alphabet = alphabets[lang];
        alphabet.split('').forEach(letter => {
            const span = document.createElement('span');
            span.textContent = letter;
            span.dataset.letter = letter;
            span.addEventListener('click', () => displayWordsByLetter(letter, container));
            alphabetFilterDiv.appendChild(span);
        });
    }

    window.onload = () => {
        document.querySelectorAll('.greeting-text[data-lang="pol"]').forEach(container => {
            initializeAlphabetFilter(container);
            displayWordsByLetter('A', container);
            container.querySelector('#searchInput').addEventListener('input', () => resetView(container));
        });

        document.querySelectorAll('.greeting-text[data-lang="eng"]').forEach(container => {
            initializeAlphabetFilter(container);
            displayWordsByLetter('A', container);
            container.querySelector('#searchInput').addEventListener('input', () => resetView(container));
        });
    };
